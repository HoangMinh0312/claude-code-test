import { render } from "@testing-library/react";
import { describe, it, expect, vi } from "vitest";
import RootLayout, { metadata } from "../layout";

vi.mock("next/font/google", () => ({
  Geist: () => ({ variable: "--font-geist-sans" }),
  Geist_Mono: () => ({ variable: "--font-geist-mono" }),
}));

vi.mock("../globals.css", () => ({}));

describe("RootLayout", () => {
  it("renders children", () => {
    const { getByText } = render(
      <RootLayout>
        <div>hello</div>
      </RootLayout>
    );
    expect(getByText("hello")).toBeTruthy();
  });

  it("applies font CSS variables and antialiased to body", () => {
    const { container } = render(
      <RootLayout>
        <span />
      </RootLayout>
    );
    const body = container.querySelector("body");
    expect(body?.className).toContain("--font-geist-sans");
    expect(body?.className).toContain("--font-geist-mono");
    expect(body?.className).toContain("antialiased");
  });

  it("sets lang='en' on html element", () => {
    const { container } = render(
      <RootLayout>
        <span />
      </RootLayout>
    );
    const html = container.querySelector("html");
    expect(html?.getAttribute("lang")).toBe("en");
  });
});

describe("metadata", () => {
  it("has correct title", () => {
    expect(metadata.title).toBe("Create Next App");
  });

  it("has correct description", () => {
    expect(metadata.description).toBe("Generated by create next app");
  });
});
